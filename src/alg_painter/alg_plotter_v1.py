def plotter_v1_main(args: argparse.Namespace) -> None:
    """
    Main orchestration — mirrors the bottom half of the R script.
    Can be called from the CLI or imported and called with an args namespace.
    """
    if args.index is None:
        location_set = prepare_data(args.file)
    else:
        location_set = prepare_data_with_index(args.file, args.index)

    locations_filt = filter_buscos(location_set, args.minimum)

    # ---- Reporting --------------------------------------------------------
    total_contigs = location_set["query_chr"].nunique()
    num_contigs = locations_filt["query_chr"].nunique()
    num_removed = total_contigs - num_contigs

    logger.info(
        f"[Plotter V1] Number of contigs before filtering by number of BUSCOs: {total_contigs}"
    )
    logger.info(
        f"[Plotter V1] Number of contigs removed by filtering : {num_removed}"
    )
    logger.info(
        f"[Plotter V1] Number of contigs post-filtering: {num_contigs}"
    )

    num_contigs_str = str(num_contigs)
    bw = args.bar_width
    bh = args.bar_height

    # ---- Select plot function based on flags ------------------------------
    if args.merians:
        subset_merians: dict[str, str] = set_merian_colour_mapping(
            locations_filt
        )

        if not args.differences:
            fig = paint_merians_all(
                locations_filt,
                1,
                args.prefix,
                num_contigs_str,
                bar_width=bw,
                bar_height=bh,
            )
        else:
            num_col = (
                1 if len(locations_filt["query_chr"].unique()) < 100 else 3
            )
            fig = paint_merians_differences_only(
                locations_filt,
                subset_merians,
                num_col,
                args.prefix,
                num_contigs_str,
                bar_width=bw,
                bar_height=bh,
            )
    else:
        if not args.differences:
            fig = paint_species_all(
                locations_filt,
                1,
                args.prefix,
                num_contigs_str,
                bar_width=bw,
                bar_height=bh,
            )
        else:
            fig = paint_species_differences_only(
                locations_filt,
                1,
                args.prefix,
                num_contigs_str,
                bar_width=bw,
                bar_height=bh,
            )

    # ---- Save outputs -----------------------------------------------------
    logger.info(f"[Plotter V1] Saving plot to: {str(args.file)}")
    save_plot(fig, str(args.file))

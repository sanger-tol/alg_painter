def _render_chromosome_plot(
    spp_df: pd.DataFrame,
    colour_map: dict,
    colour_column: str,
    chr_order: list[str],
    legend_title: str,
    title: str,
    karyotype: str,
    bar_width: float,
    bar_height: int,
    legend_order: list[str] | None = None,
) -> plt.Figure:
    """
    Core rendering function for all chromosome paint plots.

    Parameters
    ----------
    spp_df          : DataFrame with at least query_chr, position, length,
                      and the column named by ``colour_column``.
    colour_map      : {value: colour} mapping for the colour_column.
    colour_column   : Column name to look up colours from
                      (``"status"`` or ``"assigned_chr"``).
    chr_order       : Pre-sorted list of chromosomes to plot
                      (already filtered if needed).
    legend_title    : Title for the legend
                      (``"Merian element"`` or ``"Query chromosome"``).
    title           : Plot title (species name, rendered italic).
    karyotype       : Subtitle string (number of contigs).
    bar_width       : Half-width of each BUSCO marker in bp.
    bar_height      : Height of each chromosome bar.
    legend_order    : If provided, only include these keys (in order) in the
                      legend, filtered to values present in the data.
                      If ``None``, use all ``colour_map`` keys.
    """
    spp_df = spp_df.copy()
    sub_title = f"n contigs = {karyotype}"

    n_chroms = len(chr_order)
    row_height = bar_height + 4
    fig_height = max(8, n_chroms * 0.55)
    fig, ax = plt.subplots(figsize=(15 / 2.54 * 2, fig_height))

    for idx, chrom in enumerate(chr_order):
        chrom_data = spp_df[spp_df["query_chr"] == chrom]
        y = idx * row_height
        length = chrom_data["length"].iloc[0] if not chrom_data.empty else 0

        positions = chrom_data["position"].values
        colours = [
            colour_map.get(val, "lightgrey")
            for val in chrom_data[colour_column]
        ]
        _draw_chromosome_row(
            ax,
            y,
            0,
            length,
            positions,
            colours,
            bar_width=bar_width,
            bar_height=bar_height,
        )

        ax.text(
            -0.01 * spp_df["length"].max(),
            y + bar_height / 2,
            chrom,
            ha="right",
            va="center",
            fontsize=9,
        )

    # Axes formatting
    ax.set_xlim(-0.15 * spp_df["length"].max(), spp_df["length"].max() * 1.05)
    ax.set_ylim(-row_height, n_chroms * row_height)
    ax.set_xlabel("Position (Mb)", fontsize=12)
    ax.xaxis.set_major_formatter(
        plt.FuncFormatter(lambda x, _: f"{x / 1e6:.0f}")
    )
    ax.set_yticks([])

    ax.set_title(f"$\\it{{{title}}}$\n{sub_title}", fontsize=14)

    # Legend
    if legend_order is not None:
        present = set(spp_df[colour_column].unique())
        legend_keys = [k for k in legend_order if k in present]
    else:
        legend_keys = list(colour_map.keys())

    legend_handles = [
        mpatches.Patch(facecolor=colour_map.get(k, "lightgrey"), label=k)
        for k in legend_keys
    ]
    ax.legend(
        handles=legend_handles,
        title=legend_title,
        loc="center left",
        bbox_to_anchor=(1.01, 0.5),
        frameon=False,
        fontsize=9,
    )

    _apply_busco_paint_theme(ax)
    fig.tight_layout()
    return fig



def _chromosome_order(df: pd.DataFrame) -> list[str]:
    """Return chromosomes sorted by length descending (unique)."""
    chr_len = df[["query_chr", "length"]].drop_duplicates()
    chr_len = chr_len.sort_values("length", ascending=False)
    return chr_len["query_chr"].tolist()


def _apply_busco_paint_theme(ax: plt.Axes) -> None:
    """Apply the ``busco_paint_theme`` styling to an axes."""
    ax.set_facecolor("white")
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.spines["left"].set_visible(False)
    ax.tick_params(left=False, labelleft=True)
    ax.xaxis.set_tick_params(labelsize=11)


def _draw_chromosome_row(
    ax: plt.Axes,
    y: float,
    start: float,
    length: float,
    busco_positions,
    busco_colours,
    bar_width: float,
    bar_height: int,
):
    """
    Draw one chromosome row: a white backbone + coloured BUSCO ticks.
    """
    # Backbone rectangle
    rect = mpatches.FancyBboxPatch(
        (start, y),
        length,
        bar_height,
        boxstyle="square,pad=0",
        facecolor="white",
        edgecolor="black",
        linewidth=0.5,
    )
    ax.add_patch(rect)

    # BUSCO markers
    for pos, col in zip(busco_positions, busco_colours):
        busco_rect = mpatches.FancyBboxPatch(
            (pos - bar_width, y),
            bar_width * 2,
            bar_height,
            boxstyle="square,pad=0",
            facecolor=col,
            edgecolor="none",
        )
        ax.add_patch(busco_rect)



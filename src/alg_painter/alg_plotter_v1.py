def save_plot(fig: plt.Figure, base_path: str) -> None:
    """Save figure as both PNG and PDF (mirrors R script's ggsave calls)."""
    for ext in ("png", "pdf"):
        out = f"{base_path}_buscopainter.{ext}"
        fig.savefig(out, dpi=300, bbox_inches="tight")
        logger.info("[Plotter V1] Saved: %s", out)
    plt.close(fig)



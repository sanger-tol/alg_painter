def hue_palette(n: int) -> list[str]:
    """
    Replicate ggplot2's ``hue_pal()`` which generates *n* evenly-spaced
    colours around the HCL colour wheel (approximated here with HSV).
    """
    colours = []
    for i in range(n):
        hue = (i / n + 0.025) % 1.0  # slight offset matches ggplot2
        colours.append(mcolors.hsv_to_rgb((hue, 0.65, 0.95)))
    return colours


def set_merian_colour_mapping(location_set: pd.DataFrame) -> dict:
    """
    Build a mapping of Merian element → colour for elements that are
    actually present in the data.  Mirrors R's ``set_merian_colour_mapping``.
    """
    colour_palette = hue_palette(32)
    colour_palette.append("grey")  # colour for "self"

    status_merians = location_set["status"].unique().tolist()
    mapping: dict = {}
    for merian, colour in zip(MERIAN_ORDER, colour_palette):
        if merian in status_merians:
            mapping[merian] = colour
    return mapping



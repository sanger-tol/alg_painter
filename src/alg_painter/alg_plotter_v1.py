def paint_merians_differences_only(
    spp_df: pd.DataFrame,
    subset_merians: dict,
    num_col: int,
    title: str,
    karyotype: str,
    bar_width: float,
    bar_height: int,
) -> plt.Figure:
    """Plot only BUSCOs that have moved — coloured by Merian element."""
    chr_order = _chromosome_order(spp_df)
    return _render_chromosome_plot(
        spp_df,
        subset_merians,
        "status",
        chr_order,
        "Merian element",
        title,
        karyotype,
        bar_width,
        bar_height,
    )


def paint_species_differences_only(
    spp_df: pd.DataFrame,
    num_col: int,
    title: str,
    karyotype: str,
    bar_width: float,
    bar_height: int,
) -> plt.Figure:
    """Plot only BUSCOs that have moved — coloured by species/query chromosome."""
    chr_order = _chromosome_order(spp_df)
    chr_order = [c for c in chr_order if c != "self"]

    legend_levels = spp_df["status"].unique().tolist()
    legend_levels = [level for level in legend_levels if level != "self"]
    legend_levels = ["self"] + legend_levels

    col_palette_raw = hue_palette(len(legend_levels))
    col_palette_raw[0] = "grey"  # "self" → grey
    col_palette = dict(zip(legend_levels, col_palette_raw))

    return _render_chromosome_plot(
        spp_df,
        col_palette,
        "status",
        chr_order,
        "Query chromosome",
        title,
        karyotype,
        bar_width,
        bar_height,
    )


